var app=new Vue({
    el:"#app",
    data:{
        jzlist:{
            jid:'',
            jname:'',
            sex:'',
            time:''
        },
        oneCity:[],
        twoCity:[],
        thereCity:[],
        selected:{oneCity:'',twoCity:'',thereCity:''}
    },
    mounted(){
        this.findCity();
        this.findCity1();
        this.findCity2();
    },
    methods:{
        findCity(){
            axios.get('http://localhost:8080/city/queryCity?partId=0')
            .then(function(response){
                console.log(response);
                app.oneCity=response.data;
            })
            .catch(function(error){
                console.log(error);
            });
        },
        findCity1(){
            axios.get('http://localhost:8080/city/queryCity?partId=2')
            .then(function(response){
                console.log(response);
                app.twoCity=response.data;
            })
            .catch(function(error){
                console.log(error);
            });
        },
        findCity2(){
            axios.get('http://localhost:8080/city/queryCity?partId=6')
            .then(function(response){
                console.log(response);
                app.thereCity=response.data;
            })
            .catch(function(error){
                console.log(error);
            });
        },
        onSubmit(){
            axios.post('http://localhost:8080/list/add',this.jzlist)
            .then(function(response){
                console.log(response);
                location.href="index.html";
            })
            .catch(function(error){
                console.log(error);
            });
        },
        quick(){
            location.href="index.html";
        }
    }
});